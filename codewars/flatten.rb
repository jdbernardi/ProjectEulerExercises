def flatten(array)

array.flatten!(1)

print array


end


flatten([]) #, [], "[]"
flatten([1,2,3]) #, [1,2,3], "[1,2,3]" 
flatten([[1,2,3],["a","b","c"],[1,2,3]]) #, [1,2,3,"a","b","c",1,2,3], '[[1,2,3],["a","b","c"],[1,2,3]]'
flatten([[1,2,3],["a","b","c"],[1,2,3],"a"]) #, [1,2,3,"a","b","c",1,2,3,"a"], '[[1,2,3],["a","b","c"],[1,2,3],"a"]'
flatten([[3,4,5],[[9,9,9]],["a,b,c"]]) #, [3,4,5,[9,9,9],"a,b,c"], '[[3,4,5],[[9,9,9]],["a,b,c"]]'
flatten([[[3],[4],[5]],[9],[9],[8],[[1,2,3]]]) #, [[3],[4],[5],9,9,8,[1,2,3]], '[[3,4,5],[[9,9,9]],["a,b,c"]]'